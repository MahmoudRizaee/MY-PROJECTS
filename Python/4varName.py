# ~~~~~~~~~~~~~~~~~~~~~~~Python - Variable Names~~~~~~~~~~~~~~~~~~~~~~~ #
# A variables can have short name (like x and y) or a more descriptive name (age,carname, total_volume). Rules for Python Variables:
    # A variable name must start with a letter or the undersocre character.
    # A variable name cannot start with a number.
    # A variable name can only contain alpha-numeric chracters and underscores (A-z, O-9, and _)
    # Variable names are case-sensitive (age,Age and AGE are three different variables).

# Multi Words Variable Names
# Variable names with more than one word ccan be difficult to read.

# There are several techniques you can use to make them more readable:
    # Camel Case:
        # Each word, except the first, starts with a capital letter:
myVariableName = "Jim"
    # Pascal Case:
        # Each word starts with a capital letter:
MyVariableName = "Jim"
    # Snake Case:
        # Each Word is separated by an underscore character:
My_variable_name = "Jim"
