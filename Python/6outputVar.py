# ~~~~~~~~~~~~~~~~~~~~~~Output Variables~~~~~~~~~~~~~~~~~~~~ #
# The Python print statement is often used to output variables.
# To Combine both text and a variable, Python uses the + character:
x = "awesome"
print("Python is " + x)
    # You can also use the + charcter to add a variable to another variable:
x = "Python is "
y = "awesome"
z = x + y
print(z)
# NOTE: For numbers, the + character work as a mathematical operator:
x = 5
y = 10
print(x + y) # 15
# NOTE: IF you try to combine a string and a number, Python will give you an error.
